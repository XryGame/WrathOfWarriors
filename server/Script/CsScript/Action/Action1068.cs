    /// <summary>
    /// 1068_竞选加成改变
    /// </summary>
    public class Action1068 : BaseAction
    {

        private int receipt;

        public Action1068(ActionGetter actionGetter)
            : base(ActionIDDefine.Cst_Action1068, actionGetter)
        {

        }

        protected override string BuildJsonPack()
        {

            body = receipt;
            return base.BuildJsonPack();
        }

        public override bool GetUrlElement()
        {
            return true;
        }

        public override bool TakeAction()
        {
            receipt = ContextUser.JobTitleAdditionValue();
            return true;
        }
